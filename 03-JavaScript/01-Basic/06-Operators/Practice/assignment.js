let a = 2

console.log(a = 5)  // o.p:  5

console.log("Addition Assignment Operator",a += 5)     // o.p: 10

console.log("Subtraction Assignment Operator",a -= 5) // o.p:  5


console.log("Multiplication Assignment Operator",a *= 5) // o.p: 25


console.log("Division Assignment Operator",a /= 5) // o.p: 5


a = 2
console.log("Remainder Assignment Operator",a %= 5) // o.p: 2


a = 2
console.log("Exponentiation Assignment Operator",a **= 5) // o.p: 32




// -------------------------------------------------------
// Shift Assignment Operators


a = 10
console.log("Left Shift Assignment Operator",a <<= 2)  // o.p: 40

a = 10
console.log("Right Shift Assignment Operator",a >>= 2)  // o.p: 2

a = 10
console.log("Unsigned Right Shift Assignment Operator",a >>>= 2)  // o.p: 2
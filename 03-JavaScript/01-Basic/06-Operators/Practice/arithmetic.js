let a = 10, b = 5

let addition, substraction, multiplication, division, modulo, exponentiation, pre_increment, post_increment, pre_decrement, post_decrement


addition = a + b
console.log(addition);  // o.p: 15

substraction = a - b
console.log(substraction);  // o.p: 5

multiplication = a * b
console.log(multiplication);    // o.p: 50

division = a / b
console.log(division);  // o.p: 2

modulo = a % b
console.log(modulo);    // o.p: 0


exponentiation = a ** b
console.log(exponentiation);    // o.p: 100000


post_increment = a++
console.log(post_increment);  // o.p: 10


pre_increment = ++a
console.log(pre_increment);     // o.p: 12



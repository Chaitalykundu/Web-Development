// Logical AND

let x = 6, y = 3

if(x < 10 && y > 1)
    console.log(true);
else
    console.log(false); 

// o.p: true



// Logical OR
if(x == 5 || y == 5)
    console.log(true);
else
    console.log(false);

// o.p: false



// Logical NOT
if(!(x == y))
    console.log(true)
else
    console.log(false);

// o.p: true
// Use `<br>` for Line Breaks (HTML)
document.write("Hello JavaScript <br>");
document.write("This is next line");


//   Use `<p>` for Paragraph Breaks
document.write("<p>This is first paragraph</p>");
document.write("<p>This is second paragraph</p>");


// Use `\n` for Console Output (Not for HTML)
console.log("Hello JS\nWelcome to console");
